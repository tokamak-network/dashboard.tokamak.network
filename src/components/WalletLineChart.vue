<script>
import { Line, mixins } from 'vue-chartjs';
const { reactiveProp } = mixins;
export default {
  extends: Line,
  mixins: [reactiveProp],
  props: ['chartData'],
  data () {
    return {
      options: {
        scales: {
          yAxes: [{
            ticks: {
              beginAtZero: false,
            },
            gridLines: {
              display: true,
            },
          }],
          xAxes: [ {
            gridLines: {
              display: false,
            },
          }],
        },
        legend: {
          display: false,
        },
        responsive: true,
        maintainAspectRatio: true,
      },
    };
  },
  mounted () {
    this.renderChart(this.chartData, this.options);
  },
};
</script>
